"""
GGWave 16-bin code-book
-----------------------
• NUM_TONES  : 16
• ACTIVE/BIN : 2   (marker has 16)
• ORDER      : low-frequency bit = index 0

A ‘1’ marks an active tone; all others are ‘0’.
"""

# ---------------------------------------------------------------------------
# main mapping  (char → 16-bit string)
# ---------------------------------------------------------------------------

TEXT_TO_GGWAVE: dict[str, str] = {
    # space + decimal digits --------------------------------------------------
    " " : "1100000000000000",  # bins 0,1
    "0" : "1010000000000000",  # 0,2
    "1" : "1001000000000000",  # 0,3
    "2" : "1000100000000000",  # 0,4
    "3" : "1000010000000000",  # 0,5
    "4" : "1000001000000000",  # 0,6
    "5" : "1000000100000000",  # 0,7
    "6" : "1000000010000000",  # 0,8
    "7" : "1000000001000000",  # 0,9
    "8" : "1000000000100000",  # 0,10
    "9" : "1000000000010000",  # 0,11

    # A-Z ---------------------------------------------------------------------
    "A" : "1000000000001000",  # 0,12
    "B" : "1000000000000100",  # 0,13
    "C" : "1000000000000010",  # 0,14
    "D" : "1000000000000001",  # 0,15

    "E" : "0110000000000000",  # 1,2
    "F" : "0101000000000000",  # 1,3
    "G" : "0100100000000000",  # 1,4
    "H" : "0100010000000000",  # 1,5
    "I" : "0100001000000000",  # 1,6
    "J" : "0100000100000000",  # 1,7
    "K" : "0100000010000000",  # 1,8
    "L" : "0100000001000000",  # 1,9
    "M" : "0100000000100000",  # 1,10
    "N" : "0100000000010000",  # 1,11
    "O" : "0100000000001000",  # 1,12
    "P" : "0100000000000100",  # 1,13
    "Q" : "0100000000000010",  # 1,14
    "R" : "0100000000000001",  # 1,15

    "S" : "0011000000000000",  # 2,3
    "T" : "0010100000000000",  # 2,4
    "U" : "0010010000000000",  # 2,5
    "V" : "0010001000000000",  # 2,6
    "W" : "0010000100000000",  # 2,7
    "X" : "0010000010000000",  # 2,8
    "Y" : "0010000001000000",  # 2,9
    "Z" : "0010000000100000",  # 2,10

    # punctuation -------------------------------------------------------------
    "-" : "0010000000010000",  # 2,11
    "," : "0010000000001000",  # 2,12
    "." : "0010000000000100",  # 2,13
    "!" : "0010000000000010",  # 2,14
    "?" : "0010000000000001",  # 2,15

    "@" : "0001100000000000",  # 3,4
    "$" : "0001010000000000",  # 3,5
    "#" : "0001001000000000",  # 3,6

    # marker frames (preamble / postamble) ------------------------------------
    "^" : "1111111111111111",   # all 16 active
}


# Keep this alias because other code uses it
GGWAVE_CODEBOOK = TEXT_TO_GGWAVE

GGWAVE_TO_TEXT = {value:key for key, value in TEXT_TO_GGWAVE.items()}
